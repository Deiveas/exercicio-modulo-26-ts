// Função de multiplicação
function multiplicacao(numero1: number, numero2: number): number {
    return numero1 * numero2;
}

// Função de saudação
function saudacao(nome: string): string {
    return "Olá " + nome;
}

// Testando as funções
console.log(multiplicacao(5, 3)); // Saída esperada: 15
console.log(saudacao("João")); // Saída esperada: Olá João
